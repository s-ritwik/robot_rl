def filter_time_range(data: Dict[str, np.ndarray], start_time: float,
                      end_time: Optional[float]) -> Dict[str, np.ndarray]:
    """Filter data to a specific time range."""
    time = data["time"]

    start_idx = np.searchsorted(time, start_time)
    start_idx = max(0, min(start_idx, len(time) - 1))

    if end_time is not None:
        end_idx = np.searchsorted(time, end_time)
        end_idx = max(0, min(end_idx, len(time)))
    else:
        end_idx = len(time)

    filtered = {}
    for key, arr in data.items():
        filtered[key] = arr[start_idx:end_idx]

    print(f"Filtered to time range [{time[start_idx]:.2f}, {time[min(end_idx-1, len(time)-1)]:.2f}] s")
    return filtered


def main():
    parser = argparse.ArgumentParser(description='Plot control data from CSV files')
    parser.add_argument('--data', help='Path to control log folder (if not provided, uses most recent)')
    parser.add_argument('--start-time', type=float, default=0.0, help='Start time for plotting (seconds)')
    parser.add_argument('--end-time', type=float, default=None, help='End time for plotting (seconds)')
    args = parser.parse_args()

    # Determine folder to use
    if args.data:
        folder_path = args.data
        if not os.path.exists(folder_path):
            print(f"Error: Folder not found: {folder_path}")
            sys.exit(1)
    else:
        folder_path = find_most_recent_ctrl_folder()
        if folder_path is None:
            sys.exit(1)

    # Load data
    data = load_ctrl_data(folder_path)

    # Apply time filtering
    if args.start_time > 0.0 or args.end_time is not None:
        data = filter_time_range(data, args.start_time, args.end_time)

    # Generate all plots
    plot_joint_positions(data, folder_path)      # 21 joints with action targets
    plot_all_joint_positions(data, folder_path)  # All 27 joints
    plot_joint_velocities(data, folder_path)     # All 27 joints
    plot_loop_rate(data, folder_path)
    plot_commanded_velocity(data, folder_path)
    plot_angular_velocity(data, folder_path)
    plot_orientation(data, folder_path)

    print(f"\nAll plots saved to: {folder_path}")


if __name__ == "__main__":
    main()
