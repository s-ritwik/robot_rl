def plot_orientation(data: Dict[str, np.ndarray], save_dir: str) -> None:
    """Plot orientation as roll-pitch-yaw converted from quaternion."""
    time = data["time"]

    roll, pitch, yaw = quat_to_euler(
        data["quat_x"], data["quat_y"], data["quat_z"], data["quat_w"]
    )

    # Convert to degrees for easier interpretation
    roll_deg = np.rad2deg(roll)
    pitch_deg = np.rad2deg(pitch)
    yaw_deg = np.rad2deg(yaw)

    fig, axes = plt.subplots(1, 3, figsize=(15, 4))
    fig.suptitle('Orientation (Roll-Pitch-Yaw from Quaternion)', fontsize=16)

