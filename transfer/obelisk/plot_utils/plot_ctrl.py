def plot_loop_rate(data: Dict[str, np.ndarray], save_dir: str) -> None:
    """Plot loop rate (time difference between samples)."""
    time = data["time"]
    dt = np.diff(time)
    time_mid = time[:-1] + dt / 2  # Midpoints for dt values

    fig, axes = plt.subplots(1, 2, figsize=(14, 5))
    fig.suptitle('Loop Rate Analysis', fontsize=16)

    # Plot dt over time
    axes[0].plot(time_mid, dt * 1000, 'b-', linewidth=0.5)
    axes[0].set_xlabel('Time (s)')
    axes[0].set_ylabel('dt (ms)')
    axes[0].set_title('Loop Period Over Time')
    axes[0].grid(True, alpha=0.3)

    # Add statistics
    mean_dt = np.mean(dt) * 1000
    std_dt = np.std(dt) * 1000
    axes[0].axhline(y=mean_dt, color='r', linestyle='--', label=f'Mean: {mean_dt:.2f} ms')
    axes[0].legend()

    # Histogram of dt
    axes[1].hist(dt * 1000, bins=50, edgecolor='black', alpha=0.7)
    axes[1].set_xlabel('dt (ms)')
    axes[1].set_ylabel('Count')
    axes[1].set_title(f'Loop Period Distribution (mean={mean_dt:.2f}ms, std={std_dt:.2f}ms)')
    axes[1].grid(True, alpha=0.3)

    plt.tight_layout()
    output_path = os.path.join(save_dir, 'loop_rate.png')
    plt.savefig(output_path, dpi=150, bbox_inches='tight')
    plt.close()
    print(f"Saved: {output_path}")


