#!/usr/bin/env python3
"""
Script to plot control data from CSV files generated by the controller.

Usage:
    python plot_ctrl.py [--data=/path/to/folder] [--start-time=5.0] [--end-time=30.0]

If no data path is provided, uses the most recent folder from ctrl_logs.
"""

import argparse
import csv
import glob
import os
import sys
from pathlib import Path
from typing import Optional, Dict, Any

import matplotlib.pyplot as plt
import numpy as np


def find_most_recent_ctrl_folder() -> Optional[str]:
    """Find the most recent control log folder in ctrl_logs directory."""
    robot_rl_root = os.environ.get("ROBOT_RL_ROOT", "")
    if not robot_rl_root:
        print("Error: ROBOT_RL_ROOT environment variable not set")
        return None

    ctrl_logs_dir = os.path.join(robot_rl_root, "ctrl_logs")
    if not os.path.exists(ctrl_logs_dir):
        print(f"Error: ctrl_logs directory not found at {ctrl_logs_dir}")
        return None

    folders = glob.glob(os.path.join(ctrl_logs_dir, "*"))
    folders = [f for f in folders if os.path.isdir(f)]

    if not folders:
        print(f"Error: No control log folders found in {ctrl_logs_dir}")
        return None

    folders.sort(key=os.path.getmtime, reverse=True)
    most_recent = folders[0]
    print(f"Using most recent folder: {most_recent}")
    return most_recent


def load_column_names(folder_path: str) -> list[str]:
    """Load column names from fields.csv."""
    fields_path = os.path.join(folder_path, "fields.csv")
    if not os.path.exists(fields_path):
        raise FileNotFoundError(f"fields.csv not found in {folder_path}")

    with open(fields_path, 'r') as f:
        header_line = f.readline().strip()
        columns = header_line.split(',')

    print(f"Loaded {len(columns)} column names from fields.csv")
    return columns


def load_ctrl_data(folder_path: str) -> Dict[str, np.ndarray]:
    """Load control data from folder into a dictionary keyed by column name."""
    csv_path = os.path.join(folder_path, "g1_control.csv")
    if not os.path.exists(csv_path):
        raise FileNotFoundError(f"g1_control.csv not found in {folder_path}")

    columns = load_column_names(folder_path)

    # Initialize lists for each column
    data = {col: [] for col in columns}

    print(f"Loading data from: {csv_path}")

    with open(csv_path, 'r') as f:
        reader = csv.reader(f)
        for row_idx, row in enumerate(reader):
            if len(row) != len(columns):
                print(f"Warning: Row {row_idx} has {len(row)} values, expected {len(columns)}")
                continue

            for col, val in zip(columns, row):
                data[col].append(float(val))

            if row_idx % 1000 == 0 and row_idx > 0:
                print(f"Loaded {row_idx} rows...")

    # Convert to numpy arrays
    for col in columns:
        data[col] = np.array(data[col])

    n_samples = len(data["time"])
    time_span = data["time"][-1] - data["time"][0] if n_samples > 0 else 0
    print(f"Loaded {n_samples} data points spanning {time_span:.2f} seconds")

    return data


def quat_to_euler(qx: np.ndarray, qy: np.ndarray, qz: np.ndarray, qw: np.ndarray):
    """Convert quaternion to roll, pitch, yaw (in radians)."""
    # Roll (x-axis rotation)
    sinr_cosp = 2 * (qw * qx + qy * qz)
    cosr_cosp = 1 - 2 * (qx * qx + qy * qy)
    roll = np.arctan2(sinr_cosp, cosr_cosp)

    # Pitch (y-axis rotation)
    sinp = 2 * (qw * qy - qz * qx)
    pitch = np.where(np.abs(sinp) >= 1, np.sign(sinp) * np.pi / 2, np.arcsin(sinp))

    # Yaw (z-axis rotation)
    siny_cosp = 2 * (qw * qz + qx * qy)
    cosy_cosp = 1 - 2 * (qy * qy + qz * qz)
    yaw = np.arctan2(siny_cosp, cosy_cosp)

    return roll, pitch, yaw


def get_joint_names_pos(data: Dict[str, np.ndarray]) -> list[str]:
    """Extract joint names for positions from the data columns."""
    joint_names = []
    for col in data.keys():
        if col.startswith("pos_"):
            joint_name = col[4:]  # Remove "pos_" prefix
            joint_names.append(joint_name)
    return joint_names


def get_joint_names_act(data: Dict[str, np.ndarray]) -> list[str]:
    """Extract joint names for actions from the data columns."""
    joint_names = []
    for col in data.keys():
        if col.startswith("act_"):
            joint_name = col[4:]  # Remove "act_" prefix
            joint_names.append(joint_name)
    return joint_names


def plot_joint_positions(data: Dict[str, np.ndarray], save_dir: str) -> None:
    """Plot joint positions with action targets (only for joints with actions)."""
    time = data["time"]
    # Use action joint names since we want to compare pos vs action
    joint_names = get_joint_names_act(data)

    fig, axes = plt.subplots(4, 6, figsize=(24, 16))
    fig.suptitle('Joint Positions vs Action Targets', fontsize=16)

    for idx, joint_name in enumerate(joint_names):
        row = idx // 6
        col = idx % 6
        ax = axes[row, col]

        pos_key = f"pos_{joint_name}"
        act_key = f"act_{joint_name}"

        ax.plot(time, data[pos_key], 'b-', linewidth=1, label='Position', alpha=0.7)
        ax.plot(time, data[act_key], 'r-', linewidth=1, label='Action Target', alpha=0.7)

        # Shorten joint name for title
        short_name = joint_name.replace("_joint", "")
        ax.set_title(short_name, fontsize=10)
        ax.set_xlabel('Time (s)')
        ax.set_ylabel('Angle (rad)')
        ax.grid(True, alpha=0.3)
        ax.legend(fontsize=7)

    # Hide unused subplots
    for idx in range(len(joint_names), 24):
        row = idx // 6
        col = idx % 6
        axes[row, col].set_visible(False)

    plt.tight_layout()
    output_path = os.path.join(save_dir, 'joint_positions.png')
    plt.savefig(output_path, dpi=150, bbox_inches='tight')
    plt.close()
    print(f"Saved: {output_path}")


